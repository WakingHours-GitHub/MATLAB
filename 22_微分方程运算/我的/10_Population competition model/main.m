% 使用dsolve:
dsolve('Dx1 = 0.5*x1*(1-x1/300-0.5*x2/500)','Dx2=0.5*x2*(1-x2/500-2*x1/300)','x1(0)=80,x2(0)=100','t')
% [ empty sym ] -> 所以该问题使用delve求解不出来

% 下面使用ode45函数求解数值解
% 自变量为时间t, 范围为0~30, 甲乙两个种群的初始值为80, 100:
[t, x] = ode45('dx', [0, 30], [80, 100]);
% 求解得到的就是数值解
% x(1)就是第一个方程, x(2)就是第二个微分方程
plot(t, x(:, 1), '-r', t, x(:, 2), '-b')
legend('种群甲', '种群乙');
